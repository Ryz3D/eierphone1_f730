#define HW_KB_BTN_BACK(x)  HW_KB_DEF_BTN(x, 5, 1)
#define HW_KB_BTN_SMALL(x, c_btn, r_btn) HW_KB_DEF_BTN(x, c_btn, 2 + r_btn) // 6x3
#define HW_KB_BTN_BIG(x, c_btn, r_btn)   HW_KB_DEF_BTN(x, c_btn, 5 + r_btn) // 5x4

